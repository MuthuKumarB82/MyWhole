package conditioniselseandifelse;

public class FindPositiveNegative {
	public static void main(String[] args) {
		int val = 2;
	if(val>0) {
		System.out.println("+ve");
	}
	else if(val==0) {
		System.out.println("Neutral");
	}
	else if(val<0) {
		System.out.println("-ve");
	}
	else {
		System.out.println("not Defned");
	}
	}

}
