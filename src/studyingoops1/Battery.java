package studyingoops1;

public class Battery {
   private String type;
   private int power;
   
   public void setType(String type) {
	   this.type = type;
   }
   public String getType() {
	   return this.type;
   }
   public void setPower(int power) {
	   this.power = power;
   }
   public int getPower() {
	   return this.power;
   }
}
